float in = 114.151559;
float deg = 17;
int correction = 6;
//float a = 0; //just a var, used in AutoIR_rev 2


void initializeRobot()
{
	servo[bucket] = 130;
}

void accelerate(int maxSpeed, int timeToSpeed, int startSpeed, int maxDistance)
{
	maxDistance = maxDistance*in;
	int i = 0;//this will be used for the time to full speed in msec)
	int currentSpeed = startSpeed;
	motor[Left] = startSpeed;
	motor[Right] = startSpeed;
	while (i < timeToSpeed){
		if ((currentSpeed + (maxSpeed/100)) >= maxSpeed){
			motor[Left] = maxSpeed-correction;
			motor[Right] = maxSpeed;
			i = timeToSpeed;
		}
		if ((currentSpeed + (maxSpeed/100)) < maxSpeed){
			currentSpeed += (maxSpeed/100);
			motor[Left] = currentSpeed-correction;
			motor[Right] = currentSpeed;
			wait1Msec(timeToSpeed/100);
			i += (timeToSpeed/100);
		}
		if (maxDistance <= abs(nMotorEncoder[Left]))
			i = timeToSpeed;
	}
	while (maxDistance > abs(nMotorEncoder[Left])){
		motor[Left] = maxSpeed-correction;
		motor[Right] = maxSpeed;
	}
}
void decelerate(int minSpeed, int timeToSpeed, int startSpeed, int maxDistance)
{
	maxDistance = maxDistance*in;
	int i = 0;//this is the time to speed as a variable
	int currentSpeed = startSpeed;
	motor[Left] = startSpeed-correction;
	motor[Right] = startSpeed;
	while (i < timeToSpeed){
		if ((currentSpeed - ((100 - minSpeed)/100)) >= (100 - minSpeed)){
			motor[Left] = minSpeed+correction;
			motor[Right] = minSpeed;
			i = timeToSpeed;
		}
		if ((currentSpeed + ((100 - minSpeed/100))) < (100 - minSpeed)){
			currentSpeed += (100 - minSpeed);
			motor[Left] = currentSpeed+correction;
			motor[Right] = currentSpeed;
			wait1Msec(timeToSpeed);
			i += (timeToSpeed/100);
		}
		if (maxDistance <= abs(nMotorEncoder[Left])){
			motor[Left] = minSpeed+correction;
			motor[Right] = minSpeed;
			i = timeToSpeed;
		}
	}
	while (maxDistance > abs(nMotorEncoder[Left])){
		motor[Left] = ((startSpeed/abs(startSpeed)*30)+correction);
		motor[Right] = ((startSpeed/abs(startSpeed)*30));
	}
	motor[Left] = minSpeed+correction;
	motor[Right] = minSpeed;
}

void zeroEncoders()
{
	nMotorEncoder[Left] = 0;
	nMotorEncoder[Right] = 0;
}

task main()
{
	initializeRobot();
	zeroEncoders();

  waitForStart();

  int ir = 0;
  int wait = 500;
  int speed = 100;//speed of robot while moving, changes after dump cube
  int tspeed = 100;// speed of robot while turning

  //check beacon 1
  //line up with beacon 1
  zeroEncoders();
  accelerate(-speed, 500, 0, 14);
  zeroEncoders();
  decelerate(0, 100, -speed, 4);
  wait1Msec(wait);
  //check for beacon 1
  if (SensorValue[infared] == 5)
  {
    ir = 1;//beacon is on goal 1
  }
  if (ir == 0)//check beacon 2
  {
    //line up with beacon 2
    zeroEncoders();
    accelerate(-speed, 250, 0, 7);
    zeroEncoders();
    decelerate(0, 100, -speed, 3);
    wait1Msec(wait);
    //check for beacon 2
    if (SensorValue[infared] == 5)
    {
      ir = 2;//beacon is on goal 2
    }
  }
  if (ir == 0)// check beacon 3
  {
    //line up with beacon 3
    zeroEncoders();
    accelerate(-speed, 250, 0, 16);
    zeroEncoders();
    decelerate(0, 100, -speed, 4);
    wait1Msec(wait);
    //check for beacon 3
    if (SensorValue[infared] == 5)
    {
      ir = 3;//beacon is on goal 3
    }
  }
  if (ir == 0)//check beacon 4
  {
    //line up with beacon 4
    zeroEncoders();
    accelerate(-speed, 250, 0, 7);
    zeroEncoders();
    decelerate(0, 100,  -speed, 3);
    wait1Msec(wait);
    //check for beacon 4
    if (SensorValue[infared] == 5)
    {
      ir = 4;//beacon is on goal 4
    }
  }

  //align with goal
  //goal 3 and 4 are already aligned
  if (ir == 1 || ir == 2)//align 1 or 2
  {
  	zeroEncoders();
    while(abs(nMotorEncoder[Left]) < 2*in)
    {
      motor[Left] = -70;
      motor[Right] = -70;
    }
    motor[Left] = 0;
    motor[Right] = 0;
    wait1Msec(wait);
  }
  if (ir == 0)//if ir was not found, align with goal 1
  {
    zeroEncoders();
    accelerate(speed, 500, 0, 35);
    zeroEncoders();
    decelerate(0, 250, speed, 4);
    wait1Msec(wait);
  }

  //dump cube
  motor[Lift1] = 100;
  motor[Lift2] = 100;
  wait1Msec(1850);
  motor[Lift1] = 0;
  motor[Lift2] = 0;
  wait10Msec(5);
  servo[bucket] = 230;
  wait1Msec(1000);
  servo[bucket] = 125;
  wait10Msec(100);
  motor[Lift1] = -100;
  motor[Lift2] = -100;
  wait1Msec(1400);
  motor[Lift1] = 0;
  motor[Lift2] = 0;

  speed=100;

  //back up, center 18in away from end of pendulum, starting side
  if (ir == 0 || ir == 1)
  {
    zeroEncoders();
    accelerate(speed, 500, 0, 19);
    zeroEncoders();
    decelerate(0, 100, speed, 3.5);
    wait1Msec(wait);
  }
  if (ir == 2)
  {
    zeroEncoders();
    accelerate(speed, 500, 0, 29);
    zeroEncoders();
    decelerate(0, 100, speed, 3.5);
    wait1Msec(wait);
  }
  if (ir == 3)
  {
    zeroEncoders();
    accelerate(speed, 750, 0, 48) ;
    zeroEncoders();
    decelerate(0, 100, speed, 3.5);
    wait1Msec(wait);
  }
  if (ir == 4)
  {
    zeroEncoders();
    accelerate(speed, 1000, 0, 58);
    zeroEncoders();
    decelerate(0, 100, speed, 3.5);
    wait1Msec(wait);
  }

  //turn right 90deg
  zeroEncoders();
  while(abs(nMotorEncoder[Left]) < 90*deg)
  {
    motor[Left] = -tspeed;
    motor[Right] = tspeed;
  }
  motor[Left] = 0;
  motor[Right] = 0;
  wait1Msec(wait);

  //move infront of ramp
  zeroEncoders();
  accelerate(-speed, 500, 0, 28);
  zeroEncoders();
	decelerate(0, 150, -speed, 4);
  wait1Msec(wait);

  //turn so back faces ramp
  zeroEncoders();
  while(abs(nMotorEncoder[Left]) < 90*deg)
  {
    motor[Left] = -tspeed;
    motor[Right] = tspeed;
  }
  motor[Left] = 0;
  motor[Right] = 0;
  wait1Msec(wait);

  //drive up ramp
  zeroEncoders();
  while(abs(nMotorEncoder[Left]) < 34*in)
  {
    motor[Left] = -100;
    motor[Right] = -100;
  }
  motor[Left] = 0;
  motor[Right] = 0;
  wait1Msec(wait);
}
